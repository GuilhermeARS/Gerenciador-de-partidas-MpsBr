    public void ordenarLista(DefaultListModel modeloDefault){
        List lista1 = Arrays.asList(modeloDefault.toArray());
        Collections.sort(lista1);
        modeloDefault.removeAllElements();
        for (int i=0; i< lista1.size();i++){
            modeloDefault.addElement(lista1.get(i));
        }
    }
    
    
    public void removerEntreListas(){
        Campeonato camp = new Campeonato();
        camp = controller.getSelecionadoTabela(jTable1);
        if (camp != null){
        int indices[] = jList1.getSelectedIndices();
        List selectedValues = jList1.getSelectedValuesList();
            for (int i=indices.length-1; i>=0; i--){
                modelo.addElement(selectedValues.get(i));
                Time time = timeDao.getTimeByName((String) selectedValues.get(i));
                modelo1.removeElement(selectedValues.get(i));
                timeCampDao.remover(timeCampDao.retornarID(camp.getId(), time.getId()));
                tamList = modelo1.getSize();
            }
        }
    }
    
    
    public void inserirEntreListas(){
        Campeonato camp = new Campeonato();
        camp = controller.getSelecionadoTabela(jTable1);

            if (camp != null){
                int j = camp.getMax();
                int indices[] = jList3.getSelectedIndices();
                List selectedValues = jList3.getSelectedValuesList(); 
                    for (int i=indices.length-1; i>=0; i--){
                        if (tamList<j){ 
                            if (verificarItemNaoExiste(selectedValues.get(i))){
                                JOptionPane.showMessageDialog(null, "Esse time j√° foi selecionado.");
                            } else{
                                modelo1.addElement(selectedValues.get(i));
                                Time time = timeDao.getTimeByName((String) selectedValues.get(i));
                                timeCampDao.inserirTimeCampeonato(camp, time);
                                modelo.removeElement(selectedValues.get(i));
                                tamList = tamList+1;
                            }
                        } else{
                            JOptionPane.showMessageDialog(null, "Erro: N√£o √© poss√≠vel exceder o quantitativo de  " 
                                    + Integer.toString(j) + " Times. Reveja os Times selecionados.");
                            
                            break;
                        }
                    }
            }
            
    }
    
    public boolean verificarItemNaoExiste(Object item){
        boolean result = false;
        for (int i=0; i<modelo1.getSize();i++){
            if(modelo1.get(i).equals(item)){
                result = true;
                break;
            }
            else{
                result = false;
            }
        }
        return result;
    }
    
    
